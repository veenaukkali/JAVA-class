class Variables
{
public static void main(String[] args)
{
int a;  //Declaring a variable         // Int range is -2,14,74,83,648 to 2,14,74,83,648
a=10;   // Initializing variable 

System.out.println(a);




a=20; 			  // if int a=20, then it will throw error for duplicating variable. so dont declare, only initializing again new value
System.out.println(a);   // Reinitializing the variable(not declaring again, so it is not duplicated)

}
}




// OUTPUT :
//10
//20


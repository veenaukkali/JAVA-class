class Operator 
{
public static void main(String args[])
{
int a=100;
int b=200;

//**** Relational operator******
System.out.println(a==100);
System.out.println(a!=100);

//**** Logical operators --AND and OR ****

System.out.println(a==100 || b==100);  //OR will check atlease one comdition is true or not
System.out.println(a==100 && b==200);	// AND will check both the condition
}
}

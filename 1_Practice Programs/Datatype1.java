class Datatype1
{
public static void main(String[] args)
{
int a;  //Declaring a variable         // Int range is -2,14,74,83,648 to 2,14,74,83,648
a=10;   // Initializing variable 

System.out.println(a);

byte b=110;  // Byte range = -127 to 128
System.out.println(b);


short s=2736;  //Short range is -32676 to 32677
System.out.println(s);

}
}




// OUTPUT :
//10
//110
//2736
